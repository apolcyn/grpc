#!/bin/bash
set -ex

cd "$(dirname $0)"

PLATFORM="$1"
if [[ "$PLATFORM" == "" ]]; then
  echo "first argument must be a ruby gem platform"
  exit 1
fi

if ! test -d symbols; then
  echo "directory $(pwd)/symbols should exist and be populated with .dbg symbolf files before running this script"
  exit 1
fi
for f in "$(ls symbols)"; do
  echo "checking symbol file: $f"
  if [[ "$f" != *.dbg ]]; then
    echo "symbol file names should end in .dbg"
    exit 1
  fi
done

rm -r tmp || true
rm -r pkg || true
mkdir -p tmp
mkdir -p pkg

# we need to generate some package contents dynamically
cp -p grpc-native-debug.gemspec tmp
cp -p README.md tmp
cp -p version.rb tmp
cp -p platform.rb tmp
sed -i "s/# GENERATED BY BUILD_PACKAGES SCRIPT/'${PLATFORM}'/" tmp/platform.rb
mkdir -p tmp/symbols
cp -p symbols/*"$PLATFORM"*.dbg tmp/symbols

# build
cd tmp
gem build grpc-native-debug.gemspec
cp grpc-native-debug*.gem ../pkg
